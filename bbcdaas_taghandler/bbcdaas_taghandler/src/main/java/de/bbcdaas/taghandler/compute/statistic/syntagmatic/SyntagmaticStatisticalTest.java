package de.bbcdaas.taghandler.compute.statistic.syntagmatic;

/**
 * This interface is a expression of @see StatisticalTest for all syntagmatic test
 * implementations.
 * @author Robert Illers
 */ 
public interface SyntagmaticStatisticalTest {
    
    /**
     * Computes a value for each pair of terms in the term matrix, that represents
	 * the quality of the cooccurrence. 
     * @param a
     * @param b
     * @param ab
     * @return value for the syntagmatic relation
     */
    public float computeSyntagmaticRelation(long a, long b, long ab, long total);
    
}
